// x86 RTC 하드웨어 제어를 통한 실시간 시계 읽기 (상용 OS 기본 기능)
void read_rtc(rtc_time_t *t) {
    outb(0x70, 0x00); t->second = (inb(0x71) & 0x0F) + ((inb(0x71) >> 4) & 0x07) * 10;
    outb(0x70, 0x02); t->minute = (inb(0x71) & 0x0F) + ((inb(0x71) >> 4) & 0x07) * 10;
    outb(0x70, 0x04); t->hour   = (inb(0x71) & 0x0F) + ((inb(0x71) >> 4) & 0x03) * 10;
    outb(0x70, 0x07); t->day    = (inb(0x71) & 0x0F) + ((inb(0x71) >> 4) & 0x03) * 10;
    outb(0x70, 0x08); t->month  = (inb(0x71) & 0x0F) + ((inb(0x71) >> 4) & 0x01) * 10;
    outb(0x70, 0x09); t->year   = (inb(0x71) & 0x0F) + ((inb(0x71) >> 4) & 0x0F) * 10 + 2000;
}
