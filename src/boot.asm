; src/boot.asm
; 역할: 하드웨어에서 가장 먼저 실행되어 OS 커널을 메모리에 올리고 제어권을 넘김
; 방식: Multiboot v1 표준 준수 (GRUB 부트로더와 호환됨)
; 주의: 절대로 여기서 BIOS 인터럭트(int 0x10)를 호출하면 32비트 보호모드 전환에 실패합니다.

bits 32

; ==========================================
; 1. Multiboot 헤더 (부트로더가 이 파일을 OS로 인식하게 만듦)
; ==========================================
section .multiboot
align 4
    dd 0x1BADB002              ; 매직 넘버 (Multiboot v1 식별자)
    dd 0x00000003              ; 플래그 (비트 0: 그래픽 모드 요청, 비트 1: 메모리 맵 정보 요청)
    dd -(0x1BADB002 + 0x00000003) ; 체크섬 (매직 + 플래그의 합이 0이 되도록 계산)

; ==========================================
; 2. BSS 섹션 (초기화되지 않은 데이터 영역)
; 역할: 커널이 사용할 스택 메모리를 확보
; ==========================================
section .bss
align 16
stack_bottom:
    resb 16384                 ; 16KB 커널 스택 (함수 호출, 지역 변수 등에 사용)
stack_top:                   ; 스택의 끝 (높은 주소)

; ==========================================
; 3. 텍스트 섹션 (실제 실행 코드)
; 역할: 부팅 직후 가장 처음 실행되는 코드
; ==========================================
section .text
global _start
extern kernel_main             ; kernel.c에 있는 C 언어어 진입점 함수

_start:
    cli                         ; 인터럭트 비활성화 (안전한 환경 구성)
    mov esp, stack_top          ; 스택 포인터를 우리가 만든 메모리 영역으로 설정
    
    ; ==========================================
    ; Multiboot 규약 준수 처리
    ; GRUB 부트로더는 OS를 띄울 때 레지스터에 정보 구조체 주소와 매직 넘버를 남김
    ; ==========================================
    push ebx                    ; ebx = multiboot 정보 구조체 포인터 (화면 메모리 주소 등이 들어있음)
    push eax                    ; eax = 0x2BADB002 (매직 넘버, 검증용)
    
    ; C언어어 main 함수 호출 (인자 2개 전달: 매직 넘버, 정보 구조체)
    call kernel_main
    
    ; 만약 커널이 반환되면 (정상적인 OS는 반환하지 않음) 안전하게 머춤
    cli
.hang:
    hlt
    jmp .hang
